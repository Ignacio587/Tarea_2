package org.example;
/**
 * Enum que representa los distintos tipos de reuniones que se pueden agendar.
 * Puede ser utilizada para categorizar una reunión según su propósito.
 *
 * <ul>
 *     <li>{@link #TECNICA} – Reunión de carácter técnico.</li>
 *     <li>{@link #MARKETING} – Reunión relacionada con estrategias o actividades de marketing.</li>
 *     <li>{@link #OTRO} – Reunión de otro tipo no especificado.</li>
 * </ul>
 *
 * @author ignacio578
 */
public enum TipoReunion {
    TECNICA,
    MARKETING,
    OTRO
}
